-- DATES
SELECT ISSUE_DATE FROM RAW_CARDS;
-- COUNT INVALID DATES
SELECT TRY_TO_DATE(ISSUE_DATE)
FROM RAW_CARDS;

SELECT COUNT(ISSUE_DATE)
FROM RAW_CARDS
WHERE TRY_TO_DATE(ISSUE_DATE) IS NULL;

-- CORRECTING DATES ✅
SELECT 
    COALESCE(
        TRY_TO_DATE(ISSUE_DATE,'DD-MM-YYYY'),
        TRY_TO_DATE(ISSUE_DATE,'DD/MM/YYYY'),
        TRY_TO_DATE(ISSUE_DATE,'YYYY-MM-DD'),
        TRY_TO_DATE(ISSUE_DATE,'YYYY/MM/DD')
    )
FROM RAW_CARDS;

-- CHECKING AFTER CORRECTING
SELECT ISSUE_DATE FROM RAW_CARDS
WHERE TRY_TO_DATE(ISSUE_DATE,'DD-MM-YYYY') IS NULL AND
        TRY_TO_DATE(ISSUE_DATE,'DD/MM/YYYY') IS NULL AND
        TRY_TO_DATE(ISSUE_DATE,'YYYY-MM-DD') IS NULL AND
        TRY_TO_DATE(ISSUE_DATE,'YYYY/MM/DD')IS NULL ;

select EXPIRY_DATE from raw_cards
WHERE EXPIRY_DATE NOT LIKE '%/%';

--CORRECTED EXP-DATE ✅ 
select 
CASE 
WHEN EXPIRY_DATE LIKE '%-%' THEN
    TO_VARCHAR(TRY_TO_DATE(EXPIRY_DATE,'YYYY-MM-DD'),'MM/YY')
ELSE
    EXPIRY_DATE
END 
from raw_cards
