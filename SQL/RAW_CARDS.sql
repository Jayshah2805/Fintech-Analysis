-- CHECKING THE NULL VALUES AND INVALID FORMATE IN ACC ID AND CUST ID AND CARD ID
select CARD_ID,ACCOUNT_ID,CUSTOMER_ID from raw_cards
WHERE CARD_ID IS NULL OR ACCOUNT_ID IS NULL OR CUSTOMER_ID IS NULL
   OR CARD_ID NOT LIKE 'CRD%' OR ACCOUNT_ID NOT LIKE 'A%' OR CUSTOMER_ID NOT LIKE 'C%';

